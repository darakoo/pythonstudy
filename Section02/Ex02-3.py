##### 기본자료형
a1 = 10     
a2 = 10.3
a3 = '500'
a4 = 'hello'
a5 = True

# 자료형 확인하기
print(type(a1 + a2))
print(type(a2))
print(type(a3 + a4))
print(type(a5))

# 연산하기
print(a1 + a2)
print(a2)
print(a3 + a4)
print(a5)

# type 변환
print(int(a2))
print(float(a2))
print(int(a3))

# 문자열
s = 'hello'
print(s[0] + s[1] + s[2] + s[3] + s[4])
print(s[-5] + s[-4] + s[-3] + s[-2] + s[-1])
print(s[0:3])   
print(s[-3:])